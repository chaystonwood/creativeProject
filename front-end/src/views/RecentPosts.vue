<template>
<div>
  <PostList :posts="posts" />
</div>
</template>

<script>
import PostList from "../components/PostList.vue";
import axios from 'axios';
export default {
  name: 'Home',
  components: {
    PostList
  },
  data() {
    return {
     posts: [],
    }
  },
  created() {
    this.getPosts();
  },
  methods: {
    async getPosts() {
      try {
        let response = await axios.get("/api/posts");
        this.posts = response.data;
        return true;
      } catch (error) {
        console.log(error);
      }
    },
  }
}
</script>

